  static class Solution1 {

    int[] xf = {1, 1, -1, 1, -1, 0, 0, -1};
    int[] yf = {0, 1, -1, -1, 1, 1, -1, 0};
    int ans = Integer.MAX_VALUE;

    public int shortestPathBinaryMatrix(int[][] grid) {
      int n = grid.length;
      if (grid[0][0] == 1) {return -1;}
      boolean[][] visited = new boolean[n + 1][n + 1];
      backTrack(grid, visited, 0, 0, 1);
      return ans == Integer.MAX_VALUE ? -1 : ans;

    }

    public void backTrack(int[][] grid, boolean[][] visited, int x, int y, int step) {

      if (step >= ans) {return;}

      int n = grid.length;

      if (x == n - 1 && y == n - 1) {
        ans = Math.min(ans, step);
        return;
      }
      visited[x][y] = true;

      for (int i = 0; i < xf.length; ++i) {
        int newx = x + xf[i];
        int newy = y + yf[i];
        if (newx >= 0 && newx < n && newy >= 0 && newy < n && !visited[newx][newy] && grid[newx][newy] == 0) {
          backTrack(grid, visited, newx, newy, step + 1);
        }
      }
      visited[x][y] = false;

    }

    static class Solution2 {

      int[] xf = {1, 1, -1, 1, -1, 0, 0, -1};
      int[] yf = {0, 1, -1, -1, 1, 1, -1, 0};

      public int shortestPathBinaryMatrix(int[][] grid) {
        if (grid[0][0] == 1) {return -1;}
        int n = grid.length;
        int[][] dist = new int[n][n];
        for (int i = 0; i < n; ++i) {
          Arrays.fill(dist[i], Integer.MAX_VALUE);
        }
        dist[0][0] = 1;
        Deque<int[]> deque = new ArrayDeque<>();
        deque.offer(new int[]{0, 0});
        while (!deque.isEmpty()) {
          int[] arr = deque.poll();
          int x = arr[0];
          int y = arr[1];
          if (x == n - 1 && y == n - 1) {
            return dist[x][y];
          }
          for (int i = 0; i < xf.length; ++i) {
            int newx = x + xf[i];
            int newy = y + yf[i];
            if (newx < 0 || newx >= n || newy < 0 || newy >= n) {continue;}

            if (grid[newx][newy] == 1 || dist[newx][newy] <= dist[x][y] + 1) {
              continue;
            }

            dist[newx][newy] = dist[x][y] + 1;
            deque.offer(new int[]{newx, newy});

          }
        }

        return -1;
      }

    }
  }


